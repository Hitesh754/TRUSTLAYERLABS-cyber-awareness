function buildIndex<Key extends string>(items: CyberCrimeStation[], keySelector: (station: CyberCrimeStation) => Key) {
  return items.reduce<Record<string, CyberCrimeStation[]>>((acc, station) => {
    const key = keySelector(station).trim().toLowerCase();
    if (!key) {
      return acc;
    }

    acc[key] = acc[key] || [];
    acc[key].push(station);
    return acc;
  }, {});
}

